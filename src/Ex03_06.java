/**
 *  [問題3-6] 変数の宣言、値の設定、表示をするアプリケーション
 *
 * @author Unisys
 * @version 1.0
 */
public class Ex03_06 {
	/**
	 * 実行時の起点
	 *
	 * @param args String[]
	 */
	public static void main(String[] args) {
		// 変数の宣言、値の設定
		int num = 3; // 変数名  num
		int num2 = 5; // 変数名  num2
		int icode = 10; // 変数名  icode

		// 変数の値の表示
		System.out.println("変数の値を表示します");
		System.out.println(num); // 変数名  num
		System.out.println(num2); // 変数名  num2
		System.out.println(icode); // 変数名  icode
		System.out.println("以 上");
	}
}